    private void addtabledata(){
        try {
            Registry theregistry = LocateRegistry.getRegistry("127.0.0.1" ,5000);
            CollaboratorService service=(CollaboratorService)theregistry.lookup("col");
            List<Collaborator> Collalist= service.Collaboratorall();
            Iterator iterator= Collalist.iterator();
            while(iterator.hasNext()){
                Collaborator col=(Collaborator)iterator.next();
                //Department dep=departservice.findstaffDepartment(depart);
                tablemodel.addRow(new Object[]{
                col.getCo_name(),
                col.getCo_address(),
                col.getCo_contact(),
                });
                CollaTable.setModel(tablemodel);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
}
    

